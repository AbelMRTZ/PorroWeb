// ── Funciones de estadísticas ────────────────────────────────────────────────

function getWinners(cat) {
  if (cat.ganadorStats !== undefined) return cat.ganadorStats
  return MEMBERS.includes(cat.ganador) ? [cat.ganador] : []
}

function getNominees(cat) {
  if (cat.nominadosStats !== undefined) return cat.nominadosStats
  return cat.nominados.filter(n => MEMBERS.includes(n))
}

export function computeGlobalStats() {
  const stats = {}
  MEMBERS.forEach(m => { stats[m] = 0 })
  premiosData.forEach(edicion => {
    edicion.categorias.forEach(cat => {
      getWinners(cat).forEach(w => { if (stats[w] !== undefined) stats[w]++ })
    })
  })
  return Object.entries(stats)
    .map(([miembro, victorias]) => ({ miembro, victorias }))
    .sort((a, b) => b.victorias - a.victorias)
}

export function computeTotalNominations() {
  const stats = {}
  MEMBERS.forEach(m => { stats[m] = 0 })
  premiosData.forEach(edicion => {
    edicion.categorias.forEach(cat => {
      getNominees(cat).forEach(n => { if (stats[n] !== undefined) stats[n]++ })
    })
  })
  return Object.entries(stats)
    .map(([miembro, nominaciones]) => ({ miembro, nominaciones }))
    .sort((a, b) => b.nominaciones - a.nominaciones)
}

export function computeWinsByYear() {
  return premiosData.map(edicion => {
    const byMember = {}
    MEMBERS.forEach(m => { byMember[m] = 0 })
    edicion.categorias.forEach(cat => {
      getWinners(cat).forEach(w => { if (byMember[w] !== undefined) byMember[w]++ })
    })
    const sorted = Object.entries(byMember).sort((a, b) => b[1] - a[1])
    return {
      año: edicion.año,
      byMember,
      champion: sorted[0][0],
      maxVictorias: sorted[0][1],
    }
  })
}
